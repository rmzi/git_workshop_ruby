class DoMath

	def +(input_one, input_two)
		return input_one.to_i + input_two.to_i
	end

	def -(input_one, input_two)
		return input_two.to_i - input_one.to_i
	end

	def x(input_one, input_two)
		return input_one.to_i * input_two.to_i
	end

	def /(input_one, input_two)
		return input_one.to_i / input_two.to_i
	end

end